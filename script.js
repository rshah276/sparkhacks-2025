var btn = $('button');

function hoverOn() {
    $(this).css("background-color", "#f8937a"); 
    $(this).css("color", "#FFFBF4"); 
}

function hoverOff() {
    $(this).css("background-color", "#FFFBF4"); 
    $(this).css("color", "#f8937a"); 
}

btn.on("mouseenter", hoverOn);
btn.on("mouseleave", hoverOff);